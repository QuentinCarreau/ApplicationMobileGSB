package com.example.test;

public class Company {
	public int id;
	public String name;
	public String town;
	public String contact;
	public String email;
	public String phone;
	public String website;
	
	public Company (int id, String name, String town, String contact, String email, String phone, String website) {
		this.id = id;
		this.name = name;
		this.town = town;
		this.contact = contact;
		this.email = email;
		this.phone = phone;
		this.website = website;
	}
}
